  def co_icon(name, size: 17)
    raw %(<span style="display:inline-flex;width:#{size}px;height:#{size}px">#{CO_ICONS[name.to_sym]}</span>)
  end

  def co_money(v)    = CostFormat.money(v)
  def co_per_call(v) = CostFormat.per_call(v)
  def co_kfmt(n)     = CostFormat.kfmt(n)

  # A ± delta-vs-baseline chip for the cost board: down/green when cheaper than
  # baseline, up/red when dearer. Returns nil for the baseline row / no delta.
  def co_delta(pct)
    return if pct.nil? || pct.zero?

    down = pct <= 0
    css  = down ? "tp-delta tp-delta-down" : "tp-delta tp-delta-up"
    content_tag(:span, class: css) do
      content_tag(:span, (down ? "cheaper " : "dearer "), class: "sr-only") +
        co_icon(:trend_down, size: 13) + " #{down ? '−' : '+'}#{pct.abs}%"
    end
  end
